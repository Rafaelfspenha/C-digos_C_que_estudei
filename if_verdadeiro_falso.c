// if_verdadeiro_falso.c
#include <stdio.h>
int main(){
   
    if(0){
        printf("0= Verdadeiro");
    }else{
        printf("0= Falso");
    }
    
    if(1){
        printf("\n\n1= Verdadeiro");
    }else{
        printf("\n\n1= Falso");
    }
    
    if(0.5){
        printf("\n\n0.5= Verdadeiro");
    }else{
        printf("\n\n0.5= Falso");
    }
    
    if(2){
        printf("\n\n2= Verdadeiro");
    }else{
        printf("\n\n2= Falso");
    }
    
    if(-1){
        printf("\n\n-1= Verdadeiro");
    }else{
        printf("\n\n-1= Falso");
    }
    
    if(-10){
        printf("\n\n-10= Verdadeiro");
    }else{
        printf("\n\n-10= Falso");
    }
    
    printf("\n");
    return 0;
}